n1, n2, f = float(input()), float(input()), input()

if f == 'mod'and n2 != 0:
        print(n1 % n2)
elif f == 'div' and n2 != 0:
        print(n1 // n2)
elif f == 'pow':
        print(n1 ** n2)
elif f == '/' and n2 != 0:
        print(n1 / n2)
elif f == '*':
        print(n1 * n2)
elif f == '+':
        print(n1 + n2)
elif f == '-':
        print(n1 - n2)
else:
    print('Деление на 0!')
  




 